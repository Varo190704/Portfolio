    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        String name;
        String description;
        if(this.nombre.getText().isEmpty() || this.dis.getText().isEmpty() ||
           this.nombre.getText().equals("Pintura ") || this.dis.getText().equals("Renovacion de Pintura ")) {
            Vacios fallo = new Vacios();
            fallo.setVisible(true);
        }else{
            boolean bool = false;
            for(Rubro e : this.sistema.getListaRubros()){
                if(e.getNombre().equals(this.nombre.getText())){
                    e.setDescription(this.dis.getText());
                    bool = true;
                }
            }   
            if(!bool){
                name = this.nombre.getText();
                description = this.dis.getText();
                Rubro rubro = new Rubro(name, description);
                this.sistema.agregarListaRubro(rubro);
            }
            this.dispose();
        }
    }//GEN-LAST:event_addActionPerformed
    
