    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        int i = this.obras.getSelectedIndex();
        if(this.impagos.getSelectedItem()!=null){
            String s = ((String) this.impagos.getSelectedItem());
            Obra o = this.sistema.getListaObra().get(i);
            String ss = s.substring(0, s.indexOf(" -- "));
            Rubro r = o.getRubro(ss);
            ArrayList<Gasto> a = new ArrayList();
            r.setListaImpagos(a);
            this.dispose();
        }else{
            Vacios error = new Vacios();
            error.setVisible(true);
        }
    }//GEN-LAST:event_addActionPerformed

