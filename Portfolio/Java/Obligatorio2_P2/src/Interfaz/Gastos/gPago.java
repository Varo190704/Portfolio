    private void cargarImpagos() {
        impagos.removeAllItems();
        String obraSeleccionada = (String) obras.getSelectedItem();
        if (obraSeleccionada != null) {
            for (Obra c : sistema.getListaObra()) {
                if (c.toString().equals(obraSeleccionada)) {
                    for (Rubro e : c.getRubros()) {
                        for(Gasto g : e.getListaImpagos()){
                            impagos.addItem(e.getNombre() + " -- "+ g.toString());
                        }
                    }
                }
            }
        }
    }
    
