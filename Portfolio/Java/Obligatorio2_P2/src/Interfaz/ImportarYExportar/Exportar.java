    public void cedu(){
        this.sistema.ordenarCiP(ow);
        this.sistema.ordenarCiC(fo);
        ArchivoEscritura e = null;
        try {
            e = new ArchivoEscritura("Personas.txt", false);
        } catch (IOException ex) {
            eSoE eSoE = new eSoE();
            eSoE.setVisible(true);
            this.dispose();
        }
        e.grabarLinea("Propietarios");
        for(Propietario owner : ow){
            e.grabarLinea(owner.datos());
        }
        e.grabarLinea("Capataces");
        for(Capataz foreman : fo){
            e.grabarLinea(foreman.datos());
        }       
        e.cerrar();
        this.dispose();
    }
    
